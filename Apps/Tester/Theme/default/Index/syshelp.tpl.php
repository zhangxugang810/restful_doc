                    <h4>3.系统首页</h4>
                    <div class="rbleft">
                        <p>　　当你登录系统后你可以看到一个如图片的页面，这个就是系统首页，系统首页大概分为四个区域：左侧区域，右侧区域，顶部区域和底部区域。</p>
                        <p>　　其中最重要的是左侧区域和右侧区域,左侧区域是项目以及接口列表显示区域，右侧区域是接口显示区域，顶部区域的左侧是我们的LOGO，右侧是系统菜单，在菜单的下方的那个按钮是调试工具按钮，底部区域的左侧是版权信息，右侧分两部分，圆形色点为皮肤更换操作去，另外两个按钮是更新系统缓存按钮和回到顶部按钮，这些操作的具体功能下面会做详细介绍</p>
                        <p>　　左侧区域：左侧区域的上面是有一个下来列表框，这个就是项目选址列表，点击下来列表框会显示目前你能看到的所有项目，选中项目你就可以看到所有的接口分类，然后在点击任意一个接口分类你就能看到接口列表了，这个时候如果你点击任意一个接口就会在页面的右侧区域显示该接口的文档，这时候调试工具仍然是没有显示的，需要你点击页面右上角的页面调试工具按钮才可以显示页面调试工具，调试工具将在“接口的调试中”做详细的介绍。</p>
                        
                        <p>　　邮储区域：为显示区域，刚进入系统的时候回显示当前系统的一个介绍，如果您选定了一个接口，那么这里应该显示的就是您要看到的接口文档了，总之这个是一个显示区域，我来你要显示的是什么内容，只要点击相应的按钮或者链接就可以了。</p>
                        <p>　　顶部区域：顶部区域的左侧为LOGO，点击LOGO会跳转到官网首页，顶部区域的右侧是系统菜单，系统菜单一般包括两项：“系统设置”和“您的名字”，点击这两项会看到他们的下拉的二级菜单，菜单的具体功能将在“系统设置菜单”和“我的名字菜单”中在做详细介绍</p>
                        <p>　　底部区域：底部区域的左侧是版权信息，点击INKPHP会跳转到产品主页，邮储分为两部分，一部分为一些原色色点，这些色点的功能是皮肤选择，点击色点可以更换文档系统的皮肤，还有一部分是两个按钮，靠左的那个是更新系统缓存按钮，靠右的那个按钮是回到顶部按钮。</p>

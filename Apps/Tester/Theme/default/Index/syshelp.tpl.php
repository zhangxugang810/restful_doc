                    <h4>5.接口的调试</h4>
                    <div class="rbleft">
                        <p>　　接口调试按钮放在页面右上方的菜单的下面，不过接口调试工具按钮只有当你点击到文档页面的时候才有效，也就是说，只有当你的页面右侧显示区显示的是接口文档时点击接口调试工具按钮才会弹出接口调试工具，如图所示</p>
                        <p>　　接口调试工具分为以下几部分：最上面是标题栏，标题栏下面是HTTP请求头参数显示区域，再向下是POST或GET参数的显示区域，再向下有一个按钮为确认提交，这个按钮就是调试工具按钮，所有的参数都需要通过这个按钮来想接口提交发起调试请求，在按钮的下面还有一个颜色比较深的区域为接口返回参数显示区域，在这个区域你可以看到你要调试的接口给你返回的参数（目前只支持JSON），可以与文档中的结果定义进行比较，已确定返回是否按照预定程序的设定返回。</p>
                        <p>　　调试工具和文档是这个系统的两个核心部分，其他一切动能都是围绕这两个功能展开的。</p>
